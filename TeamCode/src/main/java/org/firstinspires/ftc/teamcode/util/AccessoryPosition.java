package org.firstinspires.ftc.teamcode.util;

/**
 * The {@link AccessoryPosition} of a certain subsystem
 * notates its current position, or is used to move it to a new position
 * This is often used for servos or motors to run to certain positions
 */
public enum AccessoryPosition {
    OPEN,
    CLOSED,
    UP,
    DOWN,
    MIDDLE,
    ENGAGED,
    DISENGAGED
}
